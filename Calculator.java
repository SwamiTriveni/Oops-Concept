package OopsConcept;

public class Calculator {
	int add(int num1, int num2) {
        return num1 + num2;
    }

    int add(int num1, int num2, int num3) {
        return num1 + num2 + num3;
    }

    // Multiply methods
    double multiply(double num1, double num2) {
        return num1 * num2;
    }

    float multiply(float num1, float num2) {
        return num1 * num2;

}
}
